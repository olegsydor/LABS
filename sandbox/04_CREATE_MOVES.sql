/****** It's impossible to have a transport from particular sender to particular receiver in one day  *******/
CREATE UNIQUE NONCLUSTERED INDEX IX_Moves_Sender_Receiver ON dbo.Moves
	(
	SENDER_CODE,
	RECEIVER_CODE,
    STATUS
